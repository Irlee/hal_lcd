// 定时器更新中断回调函数
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	// 判断是定时器6发生的中断
	if(htim->Instance == TIM6)
	{
		USART2_RX_STA|=1<<15;	//标记接收完成  
		HAL_TIM_Base_Stop_IT(&htim6); //关闭定时器6
	}
}

